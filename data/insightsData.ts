const hebrewMonths = [
  "ינואר", "פברואר", "מרץ", "אפריל", "מאי", "יוני",
  "יולי", "אוגוסט", "ספטמבר", "אוקטובר", "נובמבר", "דצמבר"
];

const generateStableDate = (slug: string, index: number) => {
  const date = new Date(2025, 0, 15);
  date.setDate(date.getDate() - (index * 4)); // מרווח של 4 ימים בין כתבה לכתבה
  return `${date.getDate()} ב${hebrewMonths[date.getMonth()]}, ${date.getFullYear()}`;
};

const calculateReadTime = (text: string) => {
  const words = text.split(/\s+/).length;
  return Math.max(Math.ceil(words / 180), 3);
};

export const insights: Insight[] = rawArticles.map((art, idx) => {
  const slug = `insight-${art.id}`;
  const textParts = art.text.split('\n');
  const content = textParts[0];
  const fullContent = textParts.slice(1).map(p => `<p>${p}</p>`).join('');

  return {
    slug,
    title: art.title,
    category: art.category,
    author: "ניב עיני, מנכ״ל",
    date: generateStableDate(slug, idx),
    readMinutes: calculateReadTime(art.text),
    content,
    fullContent,
    icon: art.icon,
    color: art.color,
    bgColor: art.bgColor
  };
});